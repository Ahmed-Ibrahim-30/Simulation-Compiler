const int SYMBOL_HASH_SIZE=10007;

struct LineLocation
{
    int line_num;
    LineLocation* next;
};

struct VariableInfo
{
    char* name;
    int memloc;
    int value=0;
    LineLocation* head_line; // the head of linked list of source line locations
    LineLocation* tail_line; // the tail of linked list of source line locations
    VariableInfo* next_var; // the next variable in the linked list in the same hash bucket of the symbol table
};

struct SymbolTable
{
    int num_vars;
    VariableInfo* var_info[SYMBOL_HASH_SIZE];

    SymbolTable() {num_vars=0; int i; for(i=0;i<SYMBOL_HASH_SIZE;i++) var_info[i]=0;}

    int Hash(const char* name)
    {
        int i, len=strlen(name);
        int hash_val=11;
        for(i=0;i<len;i++) hash_val=(hash_val*17+(int)name[i])%SYMBOL_HASH_SIZE;
        return hash_val;
    }

    VariableInfo* Find(const char* name)
    {   //search in the hash bucket
        int h=Hash(name);
        VariableInfo* cur=var_info[h];
        while(cur)
        {
            if(Equals(name, cur->name)) return cur;
            cur=cur->next_var;
        }
        return 0;
    }

    void Insert(const char* name, int line_num)
    {
        LineLocation* lineloc=new LineLocation;
        lineloc->line_num=line_num;
        lineloc->next=nullptr;

        int h=Hash(name);//set hash value to name
        VariableInfo* prev=nullptr;
        VariableInfo* cur=var_info[h];

        while(cur)
        {
            if(Equals(name, cur->name))
            {
                // just add this line location to the list of line locations of the existing var
                cur->tail_line->next=lineloc;
                cur->tail_line=lineloc;
                return;
            }
            prev=cur;
            cur=cur->next_var;
        }

        VariableInfo* vi=new VariableInfo;
        vi->head_line=vi->tail_line=lineloc;
        vi->next_var=0;
        vi->memloc=num_vars++;
        AllocateAndCopy(&vi->name,name);
        if(!prev) var_info[h]=vi;
        else prev->next_var=vi;
    }
    void updateValue(const char*var,int newValue){
        int i;
        for(i=0;i<SYMBOL_HASH_SIZE;i++)
        {
            VariableInfo* curv=var_info[i];
            while(curv)
            {
                if(Equals(var, curv->name)){
                    curv->value=newValue;
                    var_info[i]=curv;
                    return;
                }
                curv=curv->next_var;
            }
        }
    }

    void Print()
    {
        int i;
        for(i=0;i<SYMBOL_HASH_SIZE;i++)
        {
            VariableInfo* curv=var_info[i];
            while(curv)
            {
                printf("[Var=%s][Mem=%d][value=%d]", curv->name, curv->memloc,curv->value);
                LineLocation* curl=curv->head_line;
                while(curl)
                {
                    printf("[Line=%d]", curl->line_num);
                    curl=curl->next;
                }
                printf("\n");
                curv=curv->next_var;
            }
        }
    }

    void Destroy()
    {
        int i;
        for(i=0;i<SYMBOL_HASH_SIZE;i++)
        {
            VariableInfo* curv=var_info[i];
            while(curv)
            {
                LineLocation* curl=curv->head_line;
                while(curl)
                {
                    LineLocation* pl=curl;
                    curl=curl->next;
                    delete pl;
                }
                VariableInfo* p=curv;
                curv=curv->next_var;
                delete p;
            }
            var_info[i]=0;
        }
    }
};
SymbolTable* symbolTable;
void generateSymbolTable(TreeNode* node)//if node is id or Assign or Read insert variable into symbol table
{
    int i;
    if(node->node_kind==ID_NODE || node->node_kind==ASSIGN_NODE || node->node_kind==READ_NODE)
    {
        symbolTable->Insert(node->id,node->line_num);
    }
    for(i=0;i<MAX_CHILDREN;i++) if(node->child[i]) generateSymbolTable(node->child[i]);
    if(node->sibling) generateSymbolTable(node->sibling);
}
void readSim(const char*x){//read variable value from user
    cout<<"Enter the value of "<<x<<" :";
    int n; cin>>n;
    symbolTable->updateValue(x,n);
}
int expSim(TreeNode* node){
    if(node->node_kind==ID_NODE){
        return symbolTable->Find(node->id)->value;
    }else if(node->node_kind==NUM_NODE){
        return node->num;
    }
    if(node->node_kind==OPER_NODE){
        if(node->oper==POWER){
            int num1= expSim(node->child[1]);
            int num2= expSim(node->child[0]);
            return (int)pow(num1,num2);
        }else{
            int num1= expSim(node->child[0]);
            int num2= expSim(node->child[1]);
            if(node->oper==PLUS) return num1+num2;
            else if(node->oper==MINUS)return num1-num2;
            else if(node->oper==LESS_THAN)return num1<num2;
            else if(node->oper==EQUAL)return num1==num2;
            else if(node->oper==DIVIDE)return num1/num2;
            else if(node->oper==TIMES)return num1*num2;
        }
    }
}
void writeSim(TreeNode* node){//get variable value from symbol table and write it
    cout << expSim(node->child[0]) << endl;
}
void assignSim(TreeNode* node){
    char *var=node->id; //fact
    int value= expSim(node->child[0]);
    symbolTable->updateValue(var,value);
}
void simulateCode(TreeNode* node);
void RepeatSim(TreeNode* node){//repeat all Lines inside repeat until expression exp is false
    do{
        simulateCode(node->child[0]);
    } while (!expSim(node->child[1]));
}
bool ifSim(TreeNode* node){//return true if condition is true or else part is Found
    int valid= expSim(node->child[0]);
    if(!valid){
        if(node->child[2]) {
            simulateCode(node->child[2]);
            return true;
        }
        else return false;
    }else {
        simulateCode(node->child[1]);
        return true;
    }
}
void simulateCode(TreeNode* node){//traverse Syntax tree
    int i=0;
    if(node->node_kind == IF_NODE) {
        int valid= ifSim(node);
        if(!valid)i=MAX_CHILDREN;
        else return;
    }
    else if(node->node_kind==READ_NODE) readSim(node->id);
    else if(node->node_kind==REPEAT_NODE) {
        RepeatSim(node);
        i=MAX_CHILDREN;
    }
    else if(node->node_kind==ASSIGN_NODE) assignSim(node);
    else if(node->node_kind==WRITE_NODE) writeSim(node);
    for(i;i<MAX_CHILDREN;i++) if(node->child[i]) simulateCode(node->child[i]);
    if(node->sibling) simulateCode(node->sibling);
}
